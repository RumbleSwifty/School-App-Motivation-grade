class Student {
   final String id;
   final String name;
   final String email;
   final String classAttending;
   final String surname;
   final String phone;
   final DateTime dateOfBirth;
   final int age;
  //Constructor- initial object creation
  Student({
       required this.id,
       required this.name,
       required this.surname,
       required this.dateOfBirth,
       required this.email,
       required this.phone,
       required this.classAttending,
       required this.age,
 }); 
}